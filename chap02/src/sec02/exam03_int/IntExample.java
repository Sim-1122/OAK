package sec02.exam03_int;

public class IntExample {

	public static void main(String[] args) {
		
		// int 타입의 변수를 선언한다.
		int var1 = 10;
		
		// int 변수에 8진수를 저장한다.
		int var2 = 012;
		
		// int 변수에 16진수를 저장한다.
		int var3 = 0xA;
		
		// 출력한다.
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
	}
}
